int		ft_is_prime(int nb)
{
	int		mod;
	int		i;

	i = 2;
	if (nb <= 1)
		return (0);
	while (i < nb)
	{
		mod = nb % i;
		if (mod == 0)
			return (0);
		i++;
	}
	return (1);
}
