int		ft_strcmp(char *s1, char *s2)
{
	int		posi;

	posi = 0;
	while (s1[posi] != '\0' && s2[posi] != '\0' && s1[posi] == s2[posi])
		posi++;
	return (s1[posi] - s2[posi]);
}
